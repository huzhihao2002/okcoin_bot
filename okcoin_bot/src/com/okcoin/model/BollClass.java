package com.okcoin.model;

public class BollClass {
	long time;
	Double diff;
	Double ma;
	Double boll;
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	public Double getDiff() {
		return diff;
	}
	public void setDiff(double diff) {
		this.diff = diff;
	}	
	public Double getMa() {
		return ma;
	}
	public void setMa(double ma) {
		this.ma = ma;
	}
	public Double getBoll() {
		return boll;
	}
	public void setBoll(double boll) {
		this.boll = boll;
	}

}
